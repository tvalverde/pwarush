#!/usr/bin/env node
/**
 * Regenerates the self-hosted Material Symbols subset.
 *
 * The full variable font weighs ~4 MB; this script asks the Google Fonts CSS
 * API for a subset containing ONLY the icons listed below (keeping the wght
 * and FILL variable axes the app relies on) and stores the resulting woff2
 * plus its @font-face CSS under src/assets/fonts/.
 *
 * When a component starts using a new icon, add its name here and run:
 *   node scripts/update-material-symbols-subset.mjs
 */
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const ICON_NAMES = [
	'add',
	'arrow_back',
	'arrow_drop_down',
	'cancel',
	'chair',
	'check',
	'check_circle',
	'checkroom',
	'chevron_right',
	'close',
	'cloud_done',
	'cloud_off',
	'cloud_sync',
	'delete_forever',
	'emoji_events',
	'error',
	'fingerprint',
	'front_hand',
	'history_edu',
	'info',
	'keyboard_double_arrow_down',
	'keyboard_double_arrow_up',
	'language',
	'menu',
	'menu_book',
	'military_tech',
	'park',
	'person',
	'pets',
	'public',
	'restaurant',
	'shuffle',
	'skull',
	'sports_soccer',
	'star',
	'sunny',
	'undo',
	'update',
	'warning',
	'work',
];

const CSS_URL =
	'https://fonts.googleapis.com/css2?family=Material+Symbols+Outlined:wght,FILL@100..700,0..1' +
	`&icon_names=${ICON_NAMES.join(',')}&display=block`;

// A modern UA is required so the API serves woff2 instead of ttf.
const USER_AGENT =
	'Mozilla/5.0 (X11; Linux x86_64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0 Safari/537.36';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const fontsDir = resolve(scriptDir, '../src/assets/fonts');
const woff2Path = resolve(fontsDir, 'material-symbols-outlined-subset.woff2');
const cssPath = resolve(fontsDir, 'material-symbols-outlined.css');

const cssResponse = await fetch(CSS_URL, { headers: { 'User-Agent': USER_AGENT } });
if (!cssResponse.ok) throw new Error(`CSS request failed: ${cssResponse.status}`);
const remoteCss = await cssResponse.text();

const woff2Match = remoteCss.match(/url\((https:[^)]+)\)\s*format\('woff2'\)/);
if (!woff2Match) throw new Error(`No woff2 URL found in response:\n${remoteCss}`);

const fontResponse = await fetch(woff2Match[1], { headers: { 'User-Agent': USER_AGENT } });
if (!fontResponse.ok) throw new Error(`Font request failed: ${fontResponse.status}`);
const fontBytes = Buffer.from(await fontResponse.arrayBuffer());

const localCss = `/* Generated by scripts/update-material-symbols-subset.mjs — do not edit by hand. */
@font-face {
	font-family: "Material Symbols Outlined";
	font-style: normal;
	font-weight: 100 700;
	font-display: block;
	src: url("./material-symbols-outlined-subset.woff2") format("woff2");
}

.material-symbols-outlined {
	font-family: "Material Symbols Outlined", sans-serif;
	font-weight: normal;
	font-style: normal;
	font-size: 24px;
	line-height: 1;
	letter-spacing: normal;
	text-transform: none;
	display: inline-block;
	white-space: nowrap;
	word-wrap: normal;
	direction: ltr;
	-webkit-font-smoothing: antialiased;
	font-feature-settings: "liga";
}
`;

mkdirSync(fontsDir, { recursive: true });
writeFileSync(woff2Path, fontBytes);
writeFileSync(cssPath, localCss);
console.log(`Subset with ${ICON_NAMES.length} icons → ${fontBytes.length} bytes of woff2`);
